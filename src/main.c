void key_expansion(const unsigned char *key, unsigned char *round_keys) {
    for (int i = 0; i < KEY_SIZE; i++) {
        round_keys[i] = key[i];
    }

    for (int i = KEY_SIZE; i < AES_BLOCK_SIZE * (ROUNDS + 1); i += 4) {
        unsigned char temp[4];
        for (int j = 0; j < 4; j++) {
            temp[j] = round_keys[i - 4 + j];
        }

        if (i % KEY_SIZE == 0) {
            unsigned char t = temp[0];
            temp[0] = sbox[temp[1] >> 4][temp[1] & 0x0F];
            temp[1] = sbox[temp[2] >> 4][temp[2] & 0x0F];
            temp[2] = sbox[temp[3] >> 4][temp[3] & 0x0F];
            temp[3] = sbox[t >> 4][t & 0x0F];
            temp[0] ^= rcon[i / KEY_SIZE];
        }

        for (int j = 0; j < 4; j++) {
            round_keys[i + j] = round_keys[i - KEY_SIZE + j] ^ temp[j];
        }
    }
}

int main(void) {
    // Open the file
    FILE *file = fopen("../src/aes_sample.ans", "rb"); 
    if (!file) {
        perror("Error opening the file"); 
        return 1;
    }

    // Determine file size
    fseek(file, 0, SEEK_END); 
    long file_size = ftell(file); 
    rewind(file); 
    
    // Allocate memory for the file content
    unsigned char *buffer = (unsigned char *)malloc(file_size); 
    if (!buffer) {
        perror("Memory allocation failed."); 
        fclose(file); 
        return 1; 
    }

    // Read the file into memory
    size_t bytes_read = fread(buffer, 1, file_size, file); 
    if (bytes_read != file_size) {
        perror("File read error."); 
        free(buffer); 
        fclose(file); 
        return 1;
    }
    fclose(file); 

    // Extract the AES key (first 16 bytes)
    unsigned char key[16];
    for (int i = 0; i < 16; i++) {
        key[i] = buffer[i];
    }

    // Print the extracted key
    printf("Extracted Key:\n");
    for (int i = 0; i < 16; i++) {
        printf("%02X ", key[i]);
    }
    printf("\n\n");

    unsigned char round_keys[AES_BLOCK_SIZE * (ROUNDS + 1)];
    key_expansion(key, round_keys);

    printf("Round keys:\n");
    for (int i = 0; i < AES_BLOCK_SIZE * (ROUNDS + 1); i++) {
        printf("%02x ", round_keys[i]);
        if ((i + 1) % 16 == 0) {
            printf("\n");
        }
    }
    printf("\n");
    
    // Print the entire file content in hexadecimal
    printf("File Contents in Hex:\n");
    for (long i = 0; i < file_size; i++) {
        printf("%02X ", buffer[i]);
        if ((i + 1) % 16 == 0) {
            printf("\n");
        }
    }
    printf("\n");

    // Free allocated memory
    free(buffer); 
    return 0;
}
